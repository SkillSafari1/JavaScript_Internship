// Math operation on INT and FLOAT data Types

// SUM
var sum = 10 + 15
console.log(sum)

// SUB
var sub = 15 - 10
console.log(sub)

// Multiplication - * and for Division - /

// Reminder
var remain = 3 % 2
console.log(remain)

// To perform increments

var sample = 10
var sample = sample + 1
console.log(sample)

// increment a variable by one
sample++
console.log(sample)

sample--
console.log(sample)

// Increment or decrement of number by more numbers

var sample1 = 10
// Add 10 to sample1
sample1 +=10
console.log(sample1)

// Subtract 10 to sample1

sample1 -= 10
console.log(sample1)

// You can also multiply and divide different numbers

sample1 *= 10
console.log(sample1)

sample1 /= 5
console.log(sample1)

// Some advanced math operation for INT and Float

var num = 3.2
console.log(num)
// Simply to round the value
console.log(Math.round(num))

// To round up the value
console.log(Math.ceil(num))

// To round down the value
console.log(Math.floor(num))

